def emit_c_api_client_cpp(idl: IdlFile) -> str:
    name = idl.service_name
    lines = []
    w = lines.append

    w("// Auto-generated by ipcgen (c_api backend) — do not edit.")
    w(f'#include "{name}.h"')
    w("")
    w("namespace aether::ipc")
    w("{")

    # Constructor
    w("")
    w(f"{name}::{name}(const char *serviceName)")
    w("{")
    w("    aether_client_create(serviceName, onNotifyThunk, this, &m_cli);")
    w("}")

    # Destructor
    w("")
    w(f"{name}::~{name}()")
    w("{")
    w("    if (m_cli) aether_client_destroy(m_cli);")
    w("}")

    # connect / disconnect / isConnected
    w("")
    w(f"bool {name}::connect()")
    w("{")
    w("    return m_cli && aether_client_connect(m_cli) == AETHER_SUCCESS;")
    w("}")
    w("")
    w(f"void {name}::disconnect()")
    w("{")
    w("    if (m_cli) aether_client_disconnect(m_cli);")
    w("}")
    w("")
    w(f"bool {name}::isConnected() const")
    w("{")
    w("    return m_cli && aether_client_is_connected(m_cli);")
    w("}")

    # Typed RPC methods
    for m in idl.methods:
        in_params = [p for p in m.params if p.direction == "in"]
        out_params = [p for p in m.params if p.direction == "out"]

        parts = []
        for p in in_params:
            parts.append(_param_decl(p, idl, is_out=False))
        for p in out_params:
            parts.append(_param_decl(p, idl, is_out=True))
        parts.append("uint32_t timeoutMs")

        w("")
        w(f"int {name}::{m.name}({', '.join(parts)})")
        w("{")

        for p in in_params:
            if p.type_name == "string":
                w(f"    if ({p.name} == nullptr) return AETHER_ERR_INVALID_ARGUMENT;")

        # Marshal [in] params into a request buffer
        if in_params:
            size_parts = []
            for p in in_params:
                if p.type_name == "string":
                    size_parts.append(str(p.array_size + 1))
                else:
                    size_parts.append(f"sizeof({p.name})")
            w(f"    uint8_t req[{' + '.join(size_parts)}];")
            offset = "0"
            for i, p in enumerate(in_params):
                if p.type_name == "string":
                    buf = f"_{p.name}"
                    w(f"    char {buf}[{p.array_size + 1}] = {{}};")
                    w(f"    std::strncpy({buf}, {p.name}, {p.array_size});")
                    sz = str(p.array_size + 1)
                    src = buf
                else:
                    sz = f"sizeof({p.name})"
                    src = f"&{p.name}"
                if i == 0:
                    w(f"    std::memcpy(req, {src}, {sz});")
                    offset = sz
                else:
                    w(f"    std::memcpy(req + {offset}, {src}, {sz});")
                    offset = f"{offset} + {sz}"
        else:
            w("    const uint8_t *req = nullptr;")

        # Calculate response capacity
        if out_params:
            resp_size_parts = [_out_size_expr(p, idl) for p in out_params]
            resp_cap = " + ".join(resp_size_parts)
        else:
            resp_cap = "0"

        req_len = "sizeof(req)" if in_params else "0"

        w(f"    uint8_t resp[{resp_cap} > 0 ? {resp_cap} : 1];")
        w("    uint32_t resp_len = 0;")
        w(f"    int _rc = aether_client_call(m_cli, kServiceId, {name}::k{m.name},")
        w(f"                                 req, {req_len},")
        w(f"                                 resp, sizeof(resp), &resp_len, timeoutMs);")

        # Unmarshal [out] params
        if out_params:
            w("")
            size_parts = [_out_size_expr(p, idl) for p in out_params]
            w(f"    if (_rc == AETHER_SUCCESS && resp_len >= {' + '.join(size_parts)})")
            w("    {")
            offset = "0"
            for i, p in enumerate(out_params):
                sz = _out_size_expr(p, idl)
                if i == 0:
                    w(f"        if ({p.name})")
                    w(f"            std::memcpy({p.name}, resp, {sz});")
                    offset = sz
                else:
                    w(f"        if ({p.name})")
                    w(f"            std::memcpy({p.name}, resp + {offset}, {sz});")
                    offset = f"{offset} + {sz}"
            w("    }")

        w("    return _rc;")
        w("}")

    # Notification dispatch
    w("")
    w(f"void {name}::onNotifyThunk(uint32_t service_id, uint32_t message_id,")
    w( "                              const uint8_t *payload, uint32_t len,")
    w( "                              void *user_data)")
    w("{")
    w(f"    auto *self = static_cast<{name} *>(user_data);")
    w("    if (service_id != kServiceId) return;")
    w("")
    w("    switch (message_id)")
    w("    {")

    for n in idl.notifications:
        w(f"    case {name}::k{n.name}:")
        w("    {")

        if n.params:
            size_parts = []
            for p in n.params:
                if p.type_name == "string":
                    size_parts.append(str(p.array_size + 1))
                else:
                    size_parts.append(_wire_size(p, idl))
            expected = " + ".join(size_parts)
            w(f"        if (len < {expected}) break;")

        offset = "0"
        for i, p in enumerate(n.params):
            if p.type_name == "string":
                w(f"        char {p.name}[{p.array_size + 1}];")
                sz = str(p.array_size + 1)
                if i == 0:
                    w(f"        std::memcpy({p.name}, payload, {sz});")
                    offset = sz
                else:
                    w(f"        std::memcpy({p.name}, payload + {offset}, {sz});")
                    offset = f"{offset} + {sz}"
                w(f"        {p.name}[{p.array_size}] = '\\0';")
            else:
                ct = _cpp_param_type(p.type_name, p.array_size, idl)
                w(f"        {ct} {p.name};")
                if i == 0:
                    w(f"        std::memcpy(&{p.name}, payload, sizeof({p.name}));")
                    offset = f"sizeof({p.name})"
                else:
                    w(f"        std::memcpy(&{p.name}, payload + {offset}, sizeof({p.name}));")
                    offset = f"{offset} + sizeof({p.name})"
        call_args = ", ".join(p.name for p in n.params)
        w(f"        self->on{n.name}({call_args});")
        w("        break;")
        w("    }")

    w("    default:")
    w("        break;")
    w("    }")
    w("}")
    w("")
    w("} // namespace aether::ipc")
    w("")

    return "\n".join(lines)
