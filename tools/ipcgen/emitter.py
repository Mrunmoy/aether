def emit_client_h(idl: IdlFile) -> str:
    name = idl.service_name
    service_id = fnv1a_32(name)
    lines = []
    w = lines.append

    w("// Auto-generated by ipcgen — do not edit.")
    w("#pragma once")
    w('#include "ClientBase.h"')
    if idl.enums or idl.structs:
        w(f'#include "{name}Types.h"')
    w("#include <cstdint>")
    w("#include <cstring>")
    w("#include <vector>")
    w("")
    w("namespace ms::ipc")
    w("{")
    w("")
    w(f"class {name} : public ClientBase")
    w("{")
    w("public:")
    w("    using ClientBase::ClientBase;")
    w("")
    w(f"    static constexpr uint32_t kServiceId = 0x{service_id:08x}u;")

    _emit_enums(w, idl)

    w("")

    for m in idl.methods:
        in_params = [p for p in m.params if p.direction == "in"]
        out_params = [p for p in m.params if p.direction == "out"]
        parts = []
        for p in in_params:
            parts.append(f"{_resolve_type(p.type_name, idl)} {p.name}")
        for p in out_params:
            parts.append(f"{_resolve_type(p.type_name, idl)} *{p.name}")
        parts.append("uint32_t timeoutMs = 2000")
        w(f"    int {m.name}({', '.join(parts)});")

    w("")
    w("protected:")

    for n in idl.notifications:
        parts = [f"{_resolve_type(p.type_name, idl)} {p.name}" for p in n.params]
        w(f"    virtual void on{n.name}({', '.join(parts)}) {{}}")

    w("")
    w("    void onNotification(uint32_t serviceId, uint32_t messageId,")
    w("                        const std::vector<uint8_t> &payload) override;")
    w("};")
    w("")
    w("} // namespace ms::ipc")
    w("")

    return "\n".join(lines)


def emit_client_cpp(idl: IdlFile) -> str:
    name = idl.service_name
    lines = []
    w = lines.append

    w("// Auto-generated by ipcgen — do not edit.")
    w(f'#include "{name}.h"')
    w("")
    w("namespace ms::ipc")
    w("{")

    # Typed RPC methods
    for m in idl.methods:
        in_params = [p for p in m.params if p.direction == "in"]
        out_params = [p for p in m.params if p.direction == "out"]

        parts = []
        for p in in_params:
            parts.append(f"{_resolve_type(p.type_name, idl)} {p.name}")
        for p in out_params:
            parts.append(f"{_resolve_type(p.type_name, idl)} *{p.name}")
        parts.append("uint32_t timeoutMs")

        w("")
        w(f"int {name}::{m.name}({', '.join(parts)})")
        w("{")

        # Marshal [in] params
        if in_params:
            size_parts = [f"sizeof({p.name})" for p in in_params]
            w(f"    std::vector<uint8_t> request({' + '.join(size_parts)});")
            offset = "0"
            for i, p in enumerate(in_params):
                if i == 0:
                    w(f"    std::memcpy(request.data(), &{p.name}, sizeof({p.name}));")
                    offset = f"sizeof({p.name})"
                else:
                    w(f"    std::memcpy(request.data() + {offset}, &{p.name}, sizeof({p.name}));")
                    offset = f"{offset} + sizeof({p.name})"
        else:
            w("    std::vector<uint8_t> request;")

        w("")
        w("    std::vector<uint8_t> response;")
        w(f"    int _rc = call(kServiceId, {name}::k{m.name}, request, &response, timeoutMs);")

        # Unmarshal [out] params
        if out_params:
            w("")
            size_parts = [f"sizeof(*{p.name})" for p in out_params]
            w(f"    if (_rc == IPC_SUCCESS && response.size() >= {' + '.join(size_parts)})")
            w("    {")
            offset = "0"
            for i, p in enumerate(out_params):
                if i == 0:
                    w(f"        if ({p.name})")
                    w(f"            std::memcpy({p.name}, response.data(), sizeof(*{p.name}));")
                    offset = f"sizeof(*{p.name})"
                else:
                    w(f"        if ({p.name})")
                    w(f"            std::memcpy({p.name}, response.data() + {offset}, sizeof(*{p.name}));")
                    offset = f"{offset} + sizeof(*{p.name})"
            w("    }")

        w("    return _rc;")
        w("}")

    # onNotification dispatch
    w("")
    w(f"void {name}::onNotification(uint32_t serviceId, uint32_t messageId,")
    pad = " " * len(f"void {name}::onNotification(")
    w(f"{pad}const std::vector<uint8_t> &payload)")
    w("{")
    w("    if (serviceId != kServiceId) return;")
    w("")
    w("    switch (messageId)")
    w("    {")

    for n in idl.notifications:
        w(f"    case {name}::k{n.name}:")
        w("    {")
        offset = "0"
        for i, p in enumerate(n.params):
            ct = _resolve_type(p.type_name, idl)
            w(f"        {ct} {p.name};")
            if i == 0:
                w(f"        std::memcpy(&{p.name}, payload.data(), sizeof({p.name}));")
                offset = f"sizeof({p.name})"
            else:
                w(f"        std::memcpy(&{p.name}, payload.data() + {offset}, sizeof({p.name}));")
                offset = f"{offset} + sizeof({p.name})"
        call_args = ", ".join(p.name for p in n.params)
        w(f"        on{n.name}({call_args});")
        w("        break;")
        w("    }")

    w("    default:")
    w("        break;")
    w("    }")
    w("}")
    w("")
    w("} // namespace ms::ipc")
    w("")

    return "\n".join(lines)
