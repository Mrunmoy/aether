    if has_arrays:
        w("#include <array>")
    w("#include <cstdint>")
    w("#include <cstring>")
    w("#include <vector>")
    w("")
    w("namespace aether::ipc")
    w("{")
    w("")
    w(f"class {name} : public ClientBase")
    w("{")
    w("public:")
    w("    using ClientBase::ClientBase;")
    w("")
    w(f"    static constexpr uint32_t kServiceId = 0x{service_id:08x}u;")

    _emit_enums(w, idl)

    w("")

    for m in idl.methods:
        in_params = [p for p in m.params if p.direction == "in"]
