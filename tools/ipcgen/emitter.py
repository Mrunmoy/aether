    name = idl.service_name
    service_id = fnv1a_32(name)
    lines = []
    w = lines.append

    # Only non-string [in] params use std::array in signatures.
    has_arrays = _has_non_string_arrays(idl.methods + idl.notifications,
                                        direction_filter="in")

    w("// Auto-generated by ipcgen — do not edit.")
